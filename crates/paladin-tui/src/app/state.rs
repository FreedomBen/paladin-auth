/// Step within the in-app create-vault wizard (`docs/DESIGN.md` §6 /
/// `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Startup / vault modes").
///
/// The state machine is:
///
/// 1. [`CreateVaultStep::ChooseMode`] — user toggles between
///    [`CreateVaultMode::Encrypted`] (default) and
///    [`CreateVaultMode::Plaintext`].
/// 2. Advancing on `Encrypted` moves to
///    [`CreateVaultStep::EnterPassphrase`] with an initial
///    [`PassphraseFieldFocus::Passphrase`] focus; advancing on
///    `Plaintext` moves to [`CreateVaultStep::ConfirmPlaintext`].
///
/// `Esc` from any non-`ChooseMode` step returns to
/// `ChooseMode` (zeroizing passphrase buffers). `Esc` from
/// `ChooseMode` quits.
#[derive(Debug)]
pub enum CreateVaultStep {
    /// Initial step: user selects between Encrypted and Plaintext.
    ChooseMode {
        /// Currently selected mode (toggled by `j` / `k` / arrows).
        selection: CreateVaultMode,
    },
    /// Plaintext branch: user confirms the plaintext-storage
    /// warning before any disk write.
    ConfirmPlaintext,
    /// Encrypted branch: user types the new vault passphrase and a
    /// matching confirmation.
    EnterPassphrase {
        /// Typed passphrase characters; zeroized on cancel /
        /// mismatch / submit / `Ctrl-C`.
        passphrase: PassphraseBuffer,
        /// Confirmation buffer; same zeroize rules.
        confirmation: PassphraseBuffer,
        /// Which field currently receives keystrokes.
        focus: PassphraseFieldFocus,
    },
}

/// Mode selected in [`CreateVaultStep::ChooseMode`].
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum CreateVaultMode {
    /// Encrypted vault (recommended; default selection).
    Encrypted,
    /// Plaintext vault (insecure; requires explicit confirmation).
    Plaintext,
}

/// Focused field in [`CreateVaultStep::EnterPassphrase`].
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PassphraseFieldFocus {
    /// The `passphrase` field receives keystrokes.
    Passphrase,
    /// The `confirmation` field receives keystrokes.
    Confirmation,
}

/// Top-level UI state.
///
/// Variants other than [`AppState::Unlocked`] are deliberately
/// `Vault`/`Store`-free so the TUI cannot mutate vault data from
/// non-unlocked screens (per the plan's "non-mutating
/// startup-error" guarantee, plus the create-vault flow which only
/// reaches a mutating call after the user confirms in the final
/// step).
///
/// The `Unlocked` variant intentionally owns the live `Vault`, `Store`,
/// and per-screen UI slots (search, idle deadline, pending clipboard
/// clear, open HOTP reveal, open modal) so save-bearing effects can
/// call `Vault::mutate_and_save` / `Vault::hotp_advance` directly
/// without indirection. `AppState` is a singleton on the run loop's
/// stack, not stored in a `Vec`, so the variant-size disparity has no
/// allocator/cache impact in practice; the resulting
/// `clippy::large_enum_variant` warning is therefore allowed at the
/// enum level rather than each large field being boxed.
#[derive(Debug)]
#[allow(clippy::large_enum_variant)]
pub enum AppState {
    /// Vault file does not exist; the TUI walks the user through
    /// creating one via the in-app create-vault wizard described in
    /// `docs/DESIGN.md` §6.
    ///
    /// The state carries the resolved vault `path`, the current
    /// wizard `step` (defaults to
    /// [`CreateVaultStep::ChooseMode`] with
    /// [`CreateVaultMode::Encrypted`] selected on entry — the
    /// recommended option), and an inline `error` populated when a
    /// `Store::create` / `Vault::save` /
    /// `EncryptionOptions::new` failure surfaces from
    /// [`Effect::CreateVault`](crate::app::effect::Effect::CreateVault)
    /// or when the reducer rejects an `EnterPassphrase` submit
    /// (empty passphrase or confirmation mismatch). The flow never
    /// writes to disk before the user confirms in the final step,
    /// and on success transitions straight to [`AppState::Unlocked`]
    /// with an empty account list — the same vault + store handles
    /// a `paladin init` would produce.
    CreateVault {
        /// The vault path that was inspected.
        path: PathBuf,
        /// Current wizard step.
        step: CreateVaultStep,
        /// Inline message rendered beneath the wizard body. Usually a
        /// prior `Store::create` / `Vault::save` /
        /// `EncryptionOptions::new` failure or a reducer-side rejection
        /// ("passphrase required" / "passphrases do not match"),
        /// rendered red. The Destroy modal's success / `vault_missing`
        /// paths also land here carrying one of the neutral
        /// post-destroy confirmations
        /// ([`VAULT_DELETED`] / [`VAULT_DELETED_BACKUP_REMAINED`] /
        /// [`VAULT_ALREADY_GONE`]), which the create-vault renderer
        /// styles as a non-error notice (Milestone 10; DESIGN §6).
        /// Cleared on every step advance and on cancel.
        error: Option<String>,
    },

    /// Encrypted vault: the unlock screen is shown and the user is
    /// prompted for the passphrase inside the TUI. A
    /// `decrypt_failed` from a previous attempt is held in `error`
    /// for inline display; every other `open` error replaces this
    /// state with [`AppState::StartupError`]. Typed passphrase bytes
    /// live in `passphrase`, a zeroizing buffer cleared on submit per
    /// `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Tests > Sensitive UI buffers".
    Unlock {
        /// The vault path being unlocked.
        path: PathBuf,
        /// Inline error (most recently `decrypt_failed`), if any.
        error: Option<String>,
        /// Typed passphrase characters; zeroized on submit, cancel,
        /// modal close, and auto-lock.
        passphrase: PassphraseBuffer,
    },

    /// Auto-locked: an encrypted vault that was previously unlocked
    /// but has timed out per the auto-lock idle policy. Keeps only
    /// the resolved path plus pending clipboard-clear state needed
    /// for re-unlock and scheduled clear; the previous `Vault`,
    /// `Store`, cached key, and any modal-local secret state are
    /// discarded.
    Locked {
        /// The vault path; same as the previously unlocked state's.
        path: PathBuf,
        /// Clipboard auto-clear that was scheduled while the vault
        /// was still unlocked. Carried across the
        /// `Unlocked → Locked` transition so the timer thread's
        /// `AppEvent::ClipboardClear` wake still finds a target
        /// pending state. Per `docs/IMPLEMENTATION_PLAN_03_TUI.md`
        /// "Auto-lock (per §6)": *"A clipboard auto-clear timer
        /// scheduled before lock survives lock and still fires
        /// only-if-unchanged."*
        pending_clipboard_clear: Option<PendingClipboardClear>,
    },

    /// Unlocked: the main list view is active. Owns the `Vault` and
    /// `Store` so save-bearing effects can call
    /// `Vault::mutate_and_save`, `Vault::hotp_advance`, and
    /// passphrase-transition methods directly.
    Unlocked {
        /// The vault path the `Store` reads/writes.
        path: PathBuf,
        /// The decrypted in-memory vault.
        vault: Vault,
        /// Persistence handle for the vault file.
        store: Store,
        /// Active search-bar text used to filter the visible
        /// account list. Empty when no filter is set. Discarded on
        /// auto-lock alongside the `Vault` / `Store` per
        /// `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Auto-lock (per §6)":
        /// "the search query is cleared". Held in plain `String`
        /// because issuer / label text is non-secret (DESIGN §5);
        /// only secrets live in zeroizing storage.
        search_query: String,
        /// Auto-lock idle deadline (monotonic clock).
        ///
        /// `Some(now + timeout)` when
        /// [`paladin_core::IdlePolicy::should_arm`] holds for the
        /// current vault (encrypted **and** `auto_lock_enabled`);
        /// `None` otherwise — plaintext vaults always stay `None`
        /// per the §6 / §7 plaintext-no-op rule, and encrypted
        /// vaults with auto-lock disabled also stay `None`.
        ///
        /// Re-set on every `AppEvent::Input` and checked against
        /// monotonic `Tick` instants for the `Locked` transition.
        /// See `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Auto-lock (per §6)".
        idle_deadline: Option<Instant>,
        /// Clipboard auto-clear that was scheduled by an earlier
        /// copy effect and is still waiting for its wake-deadline.
        /// On the `Unlocked → Locked` transition this field moves
        /// onto [`AppState::Locked`] verbatim so the timer thread's
        /// wake event can still find pending state to act on. See
        /// `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Auto-lock (per §6)".
        pending_clipboard_clear: Option<PendingClipboardClear>,
        /// Open HOTP reveal window, if the user pressed `n` recently
        /// enough that the deadline returned by
        /// [`paladin_core::hotp_reveal_deadline`] has not yet
        /// elapsed. Discarded on the `Unlocked → Locked` transition
        /// alongside the `Vault` / `Store` per
        /// `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Auto-lock (per §6)":
        /// "Locking discards the Vault / Store, open HOTP reveal
        /// windows, the search query, and any modal …".
        hotp_reveal: Option<HotpReveal>,
        /// Open modal dialog, if any. Discarded on the
        /// `Unlocked → Locked` transition alongside the
        /// `Vault` / `Store` per `docs/IMPLEMENTATION_PLAN_03_TUI.md`
        /// "Auto-lock (per §6)" — modal-local zeroizing buffers
        /// (e.g. the Passphrase modal's typed bytes) zeroize on drop
        /// as their payloads land in later slices.
        modal: Option<Modal>,
        /// Currently selected account in the list view, or `None`
        /// when the filtered set is empty.
        ///
        /// On Unlocked entry the previous selection is `None`, so the
        /// initial seed follows [`paladin_core::select_after_filter`]
        /// for `prev = None` and the empty-search filtered set
        /// (`Vault::iter()` in insertion order): the first account
        /// when one exists, `None` when the vault is empty. List
        /// navigation, search filter changes, add / remove / rename,
        /// and import outcomes update this field through the
        /// reducer; the `Unlocked → Locked` transition drops it
        /// alongside the `Vault` / `Store`.
        selected: Option<AccountId>,
        /// Pending two-press vim chord leader, if the most recent
        /// list-focus keystroke was a chord leader (e.g. lower-case
        /// `g` for the `gg` jump-to-first chord). `None` when no
        /// chord is in flight.
        ///
        /// Set by the chord leader keystroke, cleared by the
        /// matching second press (which also commits the chord's
        /// action), by any non-matching key, by a focus change to
        /// the search bar, by a modal open, by `Esc`, or by the
        /// `Unlocked → Locked` auto-lock transition (which drops the
        /// field alongside the `Vault` / `Store`). No time-based
        /// clear — vim's `nottimeout` semantics.
        pending_chord_leader: Option<ChordLeader>,
        /// Number of visible list rows in the current terminal
        /// viewport. Used by page / half-page navigation
        /// (`PgUp` / `PgDn` / `Ctrl-B` / `Ctrl-F` / `Ctrl-U` /
        /// `Ctrl-D`) to compute step sizes, and by the `zz`
        /// recenter chord to keep the selected row in the middle of
        /// the viewport. Seeded to `0` on every `Unlocked` entry;
        /// the production run loop replaces it with the real
        /// terminal height through the resize-driven viewport
        /// slice (not yet implemented) before the first draw. A
        /// `0` height makes page / half-page navigation a silent
        /// no-op, which keeps reducer-only unit tests deterministic
        /// until the resize handler lands.
        viewport_height: u16,
        /// Index of the first row of the filtered list rendered at
        /// the top of the viewport (insertion-order position
        /// inside `Vault::iter()` until the search-focus slice
        /// lands, then the filtered set). Mutated by the `zz`
        /// recenter chord to
        /// `sel_pos.saturating_sub(viewport_height / 2)`, so the
        /// selected row sits in the middle of the viewport. Seeded
        /// to `0` on every `Unlocked` entry; the resize-driven
        /// viewport slice and an auto-scroll "keep selection
        /// visible" pass land later, so page / half-page / arrow
        /// navigation does not touch this field at this slice —
        /// only `zz` commits to it. With `viewport_height = 0`,
        /// `selected = None`, or an empty vault, `zz` is a silent
        /// no-op and the field stays unchanged.
        viewport_offset: u16,
        /// Which area of the main list view captures keystrokes.
        ///
        /// Seeded to [`Focus::List`] on every Unlocked-entry path
        /// per the `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Focus model"
        /// rule *"On list-view entry, focus starts on the account
        /// list"*. The keybindings that move focus to the search
        /// bar (`/`), cycle focus (`Tab` / `Shift-Tab`), and return
        /// focus to the list while clearing the query (`Esc`) land
        /// in subsequent slices. On the `Unlocked → Locked`
        /// auto-lock transition the field is discarded alongside
        /// the `Vault` / `Store` (by variant change) and re-seeded
        /// to `Focus::List` whenever Unlocked is re-entered.
        focus: Focus,
        /// Bottom-bar status message, if any. `None` on Unlocked
        /// entry; set by selection-gated action keys (`n` / `r` /
        /// `R`) when `selected` is `None` per the
        /// `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Focus model" rule, and
        /// (in later slices) by modal-success confirmations and
        /// other rejected actions. Replacement is last-write-wins.
        /// Dropped on the `Unlocked → Locked` auto-lock transition
        /// alongside the `Vault` / `Store` (by variant change).
        status_line: Option<StatusLine>,
        /// Whether the read-only Help overlay is currently visible.
        ///
        /// Per `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Help overlay":
        /// *"`?` from list focus opens a read-only Help overlay
        /// listing every keybinding from the table below; `Esc`
        /// closes the overlay and restores list focus. The overlay
        /// has no inputs and never mutates vault state."*
        ///
        /// `?` opens the overlay only when `modal` is `None`,
        /// `focus` is [`Focus::List`], and this flag is `false`.
        /// While the flag is `true`, the only key that has any
        /// effect is `Esc` (close); every other key is a silent
        /// no-op so the overlay is genuinely read-only. The flag
        /// is dropped on the `Unlocked → Locked` auto-lock
        /// transition alongside the `Vault` / `Store` (by variant
        /// change) and re-seeded to `false` whenever Unlocked is
        /// re-entered.
        help_open: bool,
    },

    /// Non-mutating startup-error screen. Used when vault-path
    /// resolution fails or `inspect` / `open` returns anything other
    /// than `decrypt_failed`. Quits on `Esc` / `q` / `Ctrl-C`.
    StartupError {
        /// Vault path, if it was resolved before the failure.
        /// `None` if `default_vault_path` itself failed.
        path: Option<PathBuf>,
        /// Pre-rendered error text. `unsafe_permissions` uses the
        /// `Some(text)` from
        /// [`paladin_core::format_unsafe_permissions`] verbatim so
        /// all front ends share identical wording.
        message: String,
    },

    /// Path-targeted vault-destroy modal (Milestone 10; DESIGN
    /// §4.3 / §6). Opened by the universal `Ctrl+Shift+D` chord from
    /// *any* other state — including over an open [`Modal`] — because
    /// the per-screen modal slot only exists on [`AppState::Unlocked`]
    /// while destroy must be reachable everywhere (the forgot-passphrase
    /// escape hatch).
    ///
    /// `prior` boxes the state the chord fired from so `Cancel` / `Esc`
    /// restores it verbatim — e.g. returning the user to the same
    /// startup-error screen, or to the unlocked list with its `Vault` /
    /// `Store` intact (any in-flight modal on the prior `Unlocked`
    /// state is closed and its secret buffers zeroized before the
    /// transition, so the restored `Unlocked` carries `modal: None`).
    /// On a successful destroy the reducer drops `prior` (wiping any
    /// held `Vault` / `Store` and secret buffers) and builds a fresh
    /// [`AppState::CreateVault`] for `path`. The boxed prior state keeps
    /// the variant from inflating every other `AppState` with an
    /// always-`None` modal slot.
    Destroy {
        /// The resolved vault path the destroy targets. Threaded
        /// straight into [`crate::app::event::Effect::DestroyVault`]
        /// on submit and into
        /// [`paladin_core::format_destroy_warning`] at open time.
        path: PathBuf,
        /// The state the `Ctrl+Shift+D` chord fired from, restored on
        /// cancel. Boxed to keep `AppState`'s stack size bounded.
        prior: Box<AppState>,
        /// Modal-local fields (warning body, confirmation buffer,
        /// focused action, inline error).
        modal: DestroyModal,
    },
}

impl AppState {
    /// Build the initial [`AppState::CreateVault`] for the given
    /// missing-vault `path`: `step =
    /// ChooseMode { selection: Encrypted }`, `error = None`.
    ///
    /// Used by [`decide_state_from_inspect`] on the
    /// [`paladin_core::VaultStatus::Missing`] branch and by tests
    /// that need a cheap initial create-vault state.
    #[must_use]
    pub fn create_vault_initial(path: PathBuf) -> Self {
        AppState::CreateVault {
            path,
            step: CreateVaultStep::ChooseMode {
                selection: CreateVaultMode::Encrypted,
            },
            error: None,
        }
    }

    /// Like [`create_vault_initial`](Self::create_vault_initial) but
    /// seeds the post-destroy confirmation line — used by the Destroy
    /// modal's success / `vault_missing` paths to land on the
    /// create-vault screen with the `Vault deleted.` (etc.)
    /// confirmation showing (Milestone 10). The note rides on the
    /// `error` slot, which the create-vault renderer styles neutrally
    /// when it matches one of the post-destroy confirmation constants.
    #[must_use]
    pub fn create_vault_with_notice(path: PathBuf, notice: impl Into<String>) -> Self {
        AppState::CreateVault {
            path,
            step: CreateVaultStep::ChooseMode {
                selection: CreateVaultMode::Encrypted,
            },
            error: Some(notice.into()),
        }
    }

    /// `true` when `message` is one of the neutral post-destroy
    /// confirmations the create-vault renderer should style as a
    /// notice rather than an error (Milestone 10). Centralized so the
    /// renderer and any future surface share one source of truth.
    #[must_use]
    pub fn is_destroy_notice(message: &str) -> bool {
        matches!(
            message,
            VAULT_DELETED | VAULT_DELETED_BACKUP_REMAINED | VAULT_ALREADY_GONE
        )
    }

    /// The resolved vault path this state operates against, if known.
    ///
    /// Every state carries a path except [`AppState::StartupError`]
    /// when `default_vault_path` itself failed (the only `None` case),
    /// in which case the Destroy chord has no path to target and the
    /// reducer treats the chord as a no-op.
    #[must_use]
    pub fn vault_path(&self) -> Option<&Path> {
        match self {
            AppState::CreateVault { path, .. }
            | AppState::Unlock { path, .. }
            | AppState::Locked { path, .. }
            | AppState::Unlocked { path, .. }
            | AppState::Destroy { path, .. } => Some(path),
            AppState::StartupError { path, .. } => path.as_deref(),
        }
    }

    /// Build an [`AppState::Destroy`] for `prior`, probing the on-disk
    /// `.bak` and sourcing the warning body from
    /// [`paladin_core::format_destroy_warning`] at open time.
    ///
    /// Returns the `prior` state unchanged (wrapped in `Err`) when it
    /// has no resolvable vault path — the only such case is a
    /// `StartupError` from a failed `default_vault_path`, where there
    /// is nothing to destroy. Callers (the reducer's chord handler)
    /// treat that as a silent no-op.
    ///
    /// The caller is responsible for closing / zeroizing any modal on
    /// the `prior` `Unlocked` state *before* boxing it here so a
    /// secret-bearing modal buffer never survives into the boxed
    /// caller state.
    pub fn open_destroy(prior: AppState) -> Result<AppState, AppState> {
        let Some(path) = prior.vault_path().map(Path::to_path_buf) else {
            return Err(prior);
        };
        let backup_present = backup_file_present(&path);
        let warning = format_destroy_warning(&path, backup_present);
        Ok(AppState::Destroy {
            path,
            prior: Box::new(prior),
            modal: DestroyModal {
                backup_present,
                warning,
                confirmation: String::new(),
                focus: DestroyAction::Cancel,
                error: None,
            },
        })
    }
}

/// Probe whether a sibling `vault.bin.bak` exists next to `path`.
///
/// Mirrors `paladin_core`'s `destroy_vault` backup probe: the suffix
/// is *appended* (`vault.bin` → `vault.bin.bak`), not substituted via
/// `Path::with_extension` (which would yield `vault.bak`). Uses
/// `symlink_metadata` so a symlinked `.bak` still counts as present —
/// the eventual `destroy_vault` call rejects it explicitly, and the
/// warning should name the path the user will be asked to wipe. Any
/// stat error other than success is treated as "absent" for the
/// purpose of the warning body; the authoritative symlink / I/O
/// handling happens in `destroy_vault` when the user confirms.
#[must_use]
pub fn backup_file_present(path: &Path) -> bool {
    let mut bak = path.as_os_str().to_os_string();
    bak.push(".bak");
    std::fs::symlink_metadata(PathBuf::from(bak)).is_ok()
}

/// Map a `paladin_core::inspect` result onto the corresponding initial
/// [`AppState`].
///
/// Returns `None` for [`VaultStatus::Plaintext`]: the caller must
/// follow up with `Store::open(path, VaultLock::Plaintext)` and feed
/// the outcome into [`decide_state_from_open`]. This split keeps the
/// pure decision logic separate from the impure `open` call so each
/// branch is unit-testable without touching the filesystem.
#[must_use]
pub fn decide_state_from_inspect(
    path: &Path,
    inspect: Result<VaultStatus, PaladinError>,
) -> Option<AppState> {
    match inspect {
        Ok(VaultStatus::Missing) => Some(AppState::create_vault_initial(path.to_path_buf())),
        Ok(VaultStatus::Encrypted) => Some(AppState::Unlock {
            path: path.to_path_buf(),
            error: None,
            passphrase: PassphraseBuffer::new(),
        }),
        Ok(VaultStatus::Plaintext) => None,
        Err(err) => Some(AppState::StartupError {
            path: Some(path.to_path_buf()),
            message: render_error_message(&err),
        }),
    }
}

/// Map a `Store::open` result onto the corresponding initial
/// [`AppState`].
///
/// `now` is the monotonic instant sampled at the boundary (right
/// after `Store::open` returns) and is used to compute the auto-lock
/// [`AppState::Unlocked::idle_deadline`] via
/// [`compute_idle_deadline`]. It is unused on the error branch.
#[must_use]
pub fn decide_state_from_open(
    now: Instant,
    path: PathBuf,
    open: Result<(Vault, Store), PaladinError>,
) -> AppState {
    match open {
        Ok((vault, store)) => {
            let idle_deadline = compute_idle_deadline(now, &vault);
            let selected = initial_selection(&vault);
            AppState::Unlocked {
                path,
                vault,
                store,
                search_query: String::new(),
                idle_deadline,
                pending_clipboard_clear: None,
                hotp_reveal: None,
                modal: None,
                selected,
                pending_chord_leader: None,
                viewport_height: 0,
                viewport_offset: 0,
                focus: Focus::List,
                status_line: None,
                help_open: false,
            }
        }
        Err(err) => AppState::StartupError {
            path: Some(path),
            message: render_error_message(&err),
        },
    }
}

/// Compute the initial `AppState::Unlocked::selected` for `vault`.
///
/// Mirrors [`paladin_core::select_after_filter`] for `prev = None` and
/// the empty-search filtered set (`Vault::iter()` in insertion
/// order): the first account when one exists, `None` when the vault
/// is empty. Used on every Unlocked-entry site so the user lands on
/// a navigable selection without an extra arrow-key press.
#[must_use]
pub fn initial_selection(vault: &Vault) -> Option<AccountId> {
    vault.iter().next().map(paladin_core::Account::id)
}

/// Compute the auto-lock idle deadline for the given vault at `now`.
///
/// Thin wrapper around [`paladin_core::IdlePolicy::next_deadline`]
/// that pulls `is_encrypted` and `settings()` off the [`Vault`] so
/// every Unlocked-entry site uses the same call shape. Returns
/// `None` for plaintext vaults (the §6 / §7 plaintext-no-op rule)
/// and for encrypted vaults whose `auto_lock_enabled` setting is
/// `false`; otherwise returns `Some(now + timeout)`.
#[must_use]
pub fn compute_idle_deadline(now: Instant, vault: &Vault) -> Option<Instant> {
    IdlePolicy::next_deadline(now, vault.is_encrypted(), vault.settings())
}

/// Render an error for the startup-error screen.
///
/// `unsafe_permissions` errors use the `Some(text)` from
/// [`paladin_core::format_unsafe_permissions`] verbatim so the CLI,
/// TUI, and GUI surface identical wording; every other error falls
/// back to the error's `Display` implementation.
#[must_use]
pub fn render_error_message(error: &PaladinError) -> String {
    format_unsafe_permissions(error).unwrap_or_else(|| error.to_string())
}

/// Render the inline error label the Destroy modal shows when
/// [`paladin_core::destroy_vault`] returns a non-`vault_missing`
/// failure (DESIGN §4.3 / §6, Milestone 10).
///
/// The modal stays open on these paths so the user can see exactly
/// what is — and is not — on disk before deciding whether to retry or
/// drop to a shell. The wording names the failing path and, for the
/// post-primary [`paladin_core::PaladinError::DestroyIoError`] cases,
/// reflects the partial [`paladin_core::DestroyReport`] so it is
/// unambiguous which file survived:
///
/// * `unlink_backup_file` → `Primary deleted; backup unlink failed: <bak>`
///   — the primary is gone, the `.bak` remains.
/// * `fsync_vault_dir` → `Vault unlinked but durability unconfirmed: <dir>`
///   — the unlinks happened but the parent-directory `fsync` failed,
///   so the deletion may not survive a crash.
/// * pre-primary `io_error` (`vault_file_is_symlink` /
///   `backup_file_is_symlink` / `unlink_vault_file`) → a path-named
///   line; nothing was unlinked.
///
/// `vault_path` is the resolved primary path the modal targeted;
/// `core` does not carry the path on these errors, so the modal
/// threads it in. The `.bak` / parent-dir paths are derived from it
/// the same way `destroy_vault` derives them.
#[must_use]
pub fn render_destroy_error(error: &PaladinError, vault_path: &Path) -> String {
    let bak_path = {
        let mut bak = vault_path.as_os_str().to_os_string();
        bak.push(".bak");
        PathBuf::from(bak)
    };
    match error {
        PaladinError::DestroyIoError { operation, .. } => match *operation {
            "unlink_backup_file" => {
                format!(
                    "Primary deleted; backup unlink failed: {}",
                    bak_path.display()
                )
            }
            "fsync_vault_dir" => {
                let dir = vault_path.parent().unwrap_or(vault_path);
                format!(
                    "Vault unlinked but durability unconfirmed: {}",
                    dir.display()
                )
            }
            // Defensive: any future `DestroyIoError` operation falls
            // back to naming the primary path so the modal never shows
            // an empty error.
            other => format!("Destroy failed ({other}): {}", vault_path.display()),
        },
        PaladinError::IoError { operation, .. } => match *operation {
            "vault_file_is_symlink" => {
                format!(
                    "Vault file is a symlink, refusing to follow: {}",
                    vault_path.display()
                )
            }
            "backup_file_is_symlink" => {
                format!(
                    "Backup file is a symlink, refusing to follow: {}",
                    bak_path.display()
                )
            }
            "unlink_vault_file" => {
                format!("Could not delete vault file: {}", vault_path.display())
            }
            // Any other pre-primary I/O error (e.g. a stat failure)
            // names the primary path and falls back to the core
            // wording so the cause is still visible.
            _ => format!("{}: {}", error, vault_path.display()),
        },
        // Non-I/O errors (should not occur for `destroy_vault`, which
        // never opens / decrypts) fall back to the shared renderer so
        // the modal still surfaces something actionable.
        _ => render_error_message(error),
    }
}

/// Path-aware error renderer for the in-app create-vault wizard.
///
/// Routes `create_vault_dir` `IoError`s through
/// [`paladin_core::format_create_vault_dir_error`] so the wizard's
/// inline error names the directory paladin tried to `mkdir -p`; every
/// other error falls back to [`render_error_message`] so wording stays
/// identical to the rest of the TUI.
///
/// `vault_path` is the path the user submitted to the wizard — the
/// helper threads `vault_path.parent()` into the formatter because
/// `PaladinError::IoError` does not carry the attempted path.
#[must_use]
pub fn render_create_vault_error_message(
    error: &PaladinError,
    vault_path: &std::path::Path,
) -> String {
    let attempted_dir = vault_path
        .parent()
        .map(std::path::Path::to_path_buf)
        .unwrap_or_default();
    format_create_vault_dir_error(error, &attempted_dir)
        .unwrap_or_else(|| render_error_message(error))
}

/// Render the `"issuer:label"` (or bare `"label"`) display string
/// for an [`AccountSummary`]. Mirrors the CLI's `display_label` so
/// duplicate-account messages and other surfaces use identical
/// wording across front ends.
#[must_use]
pub fn format_account_display_label(summary: &AccountSummary) -> String {
    match &summary.issuer {
        Some(issuer) => format!("{}:{}", issuer, summary.label),
        None => summary.label.clone(),
    }
}

/// Render the inline `duplicate_account` rejection message shown in
/// [`AddModal::error`] when [`paladin_core::Vault::find_duplicate`]
/// matches an existing account on the
/// `(secret, issuer, label)` triple.
///
/// Per `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Modals (per §6)" > Add: *"A
/// collision initially rejects with the existing account in the
/// modal and offers an 'add anyway' confirmation."* The wording
/// mirrors the CLI's `duplicate_account` rendering and names the
/// existing account so the user can confirm it really is the
/// duplicate they expect; the "add anyway" hint matches the TUI's
/// modal-local confirm gesture rather than the CLI's flag.
#[must_use]
pub fn format_duplicate_account_message(existing: &AccountSummary) -> String {
    format!(
        "account already exists with the same (secret, issuer, label): {} (press Enter to add anyway)",
        format_account_display_label(existing),
    )
}

/// Render the inline error message shown in [`AddModal::error`] when
/// a clipboard QR-import attempt fails before reaching the importer
/// (the `arboard` read returned no image, or the image bytes could
/// not be decoded), or when the importer / save layer rejects the
/// decoded payload.
///
/// Per `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Add modal": *"No-image,
/// no-QR, and invalid-QR cases reject inline."* The TUI-side
/// `arboard` failures ("no image" / "image decode") have no
/// equivalent in the core's `PaladinError` vocabulary because they
/// originate at the front-end clipboard adapter, so the wording
/// lives here. Importer-side failures (oversized buffer, no QRs
/// decoded, non-otpauth payloads, save errors, etc.) carry a
/// [`PaladinError`] in
/// [`QrImportFailure::Import`](crate::app::event::QrImportFailure::Import)
/// and reuse [`render_error_message`] for parity with the rest of
/// the TUI's error surface.
#[must_use]
pub fn format_qr_import_failure(failure: &crate::app::event::QrImportFailure) -> String {
    use crate::app::event::QrImportFailure;
    match failure {
        QrImportFailure::NoClipboardImage => {
            "QR import failed: clipboard does not contain an image (copy a QR image first)."
                .to_string()
        }
        QrImportFailure::ImageDecodeFailure => {
            "QR import failed: clipboard image could not be decoded.".to_string()
        }
        QrImportFailure::Import(err) => render_error_message(err),
    }
}

/// Build the TUI's initial state from the optional `--vault`
/// command-line override.
///
/// Mirrors `docs/IMPLEMENTATION_PLAN_03_TUI.md` "Startup / vault modes":
///
/// 1. Resolve the vault path (`--vault` overrides `default_vault_path`).
/// 2. Call [`paladin_core::inspect`].
/// 3. Branch on [`VaultStatus`]; plaintext vaults are opened
///    immediately, encrypted vaults defer to the unlock screen,
///    missing vaults open the in-app create-vault flow (see
///    [`AppState::create_vault_initial`]), and any error from
///    path resolution / `inspect` / plaintext `open` lands on the
///    non-mutating startup-error screen.
pub fn build_initial_state(vault: Option<PathBuf>) -> AppState {
    build_initial_state_with_resolver(vault, paladin_core::default_vault_path)
}

/// Identical to [`build_initial_state`] but takes an injectable
/// resolver for the default vault path. Production callers use
/// [`build_initial_state`], which wires
/// [`paladin_core::default_vault_path`] as the resolver. Tests use this
/// entry point to drive the resolver-failure branch on hosts where
/// `ProjectDirs::from` always succeeds.
pub fn build_initial_state_with_resolver<F>(vault: Option<PathBuf>, resolve_default: F) -> AppState
where
    F: FnOnce() -> paladin_core::Result<PathBuf>,
{
    let path: PathBuf = match vault {
        Some(p) => p,
        None => match resolve_default() {
            Ok(p) => p,
            Err(err) => {
                return AppState::StartupError {
                    path: None,
                    message: render_error_message(&err),
                };
            }
        },
    };

    let inspect = paladin_core::inspect(&path);
    if let Some(state) = decide_state_from_inspect(&path, inspect) {
        return state;
    }
    // `VaultStatus::Plaintext` — open immediately, no passphrase prompt.
    let open = Store::open(&path, VaultLock::Plaintext);
    // Sample `now` at the boundary so the auto-lock deadline math
    // sees the same instant the open completed at. For plaintext
    // vaults the deadline is always `None`; the sample is still
    // taken to keep the call shape uniform with encrypted unlock.
    let now = Instant::now();
    decide_state_from_open(now, path, open)
}
