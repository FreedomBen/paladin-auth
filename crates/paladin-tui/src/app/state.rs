/// A clipboard auto-clear scheduled by an earlier copy effect and
/// awaiting either its wake-deadline (at which point the bytes are
/// wiped if they still match the system clipboard) or a stale-token
/// supersession.
///
/// Held on [`AppState::Unlocked`] while the user is active and on
/// [`AppState::Locked`] when an idle-expiry transition arrives before
/// the wake fires — per `IMPLEMENTATION_PLAN_03_TUI.md`
/// "Auto-lock (per §6)": *"A clipboard auto-clear timer scheduled
/// before lock survives lock and still fires only-if-unchanged."*
///
/// `value` is the bytes that were written to the clipboard; the
/// reducer compares them to the current clipboard contents via
/// [`paladin_core::ClipboardClearPolicy::should_clear`] when the wake
/// event arrives so a later external copy is preserved. The bytes are
/// non-secret-token-bearing on their own but reflect a code that was
/// just exposed to the user, so the same zeroization discipline as
/// other typed buffers applies — the field is wrapped in
/// [`Zeroizing<Vec<u8>>`] so `Drop` wipes the bytes in place before
/// the backing allocation is freed (covers the "after the clear
/// attempt" path when the executor consumes
/// [`Effect::ClearClipboard`] and the "supersession drop" path when
/// a fresher copy replaces the pending slot).
#[derive(Debug)]
pub struct PendingClipboardClear {
    /// Monotonic token returned by
    /// [`paladin_core::ClipboardClearPolicy::schedule`]. A later
    /// schedule on the same vault settings supersedes this one.
    pub token: ClipboardClearToken,
    /// The bytes the copy effect wrote to the clipboard. Compared
    /// byte-equal against the current clipboard contents when the
    /// wake fires; only-if-unchanged. Wrapped in [`Zeroizing`] so the
    /// bytes are wiped on drop.
    pub value: Zeroizing<Vec<u8>>,
    /// Monotonic wake-deadline; the timer thread sleeps until this
    /// instant and then sends an `AppEvent::ClipboardClear`.
    pub deadline: Instant,
}

/// An open HOTP reveal window: a code generated by `Vault::hotp_advance`
/// being shown to the user until either
/// [`paladin_core::HOTP_REVEAL_SECS`] elapses or `n` advances the
/// counter again.
///
/// Held on [`AppState::Unlocked`] only. Dropped on the
/// `Unlocked → Locked` auto-lock transition alongside the
/// `Vault` / `Store` per `IMPLEMENTATION_PLAN_03_TUI.md`
/// "Auto-lock (per §6)": *"Locking discards the Vault / Store, open
/// HOTP reveal windows, the search query, and any modal while
/// retaining the resolved vault path for the next unlock attempt."*
///
/// `code` is a recently displayed OTP and is held in
/// [`secrecy::SecretString`] so its bytes zeroize on drop; the full
/// zeroize-on-expiry / replacement / lock coverage rides on the
/// "Sensitive UI buffers" slice when the HOTP-reveal effect lands.
#[derive(Debug)]
pub struct HotpReveal {
    /// The account whose code is being revealed.
    pub account_id: AccountId,
    /// The counter value that produced the visible `code`; shown in
    /// place of the next-counter prompt until the deadline fires.
    pub counter_used: u64,
    /// The displayed code. Zeroizes on drop via
    /// [`secrecy::SecretString`].
    pub code: SecretString,
    /// Monotonic reveal-expiry returned by
    /// [`paladin_core::hotp_reveal_deadline`]; on each Tick the
    /// reducer compares this against `monotonic` and closes the
    /// reveal when expired.
    pub deadline: Instant,
}

/// A bottom-bar status message shown on the list view.
///
/// Set by actions that need to surface a short, non-modal message —
/// e.g. pressing `n` / `r` / `R` (and later `Enter`) with no account
/// selected per `IMPLEMENTATION_PLAN_03_TUI.md` "Focus model" — and
/// cleared on the `Unlocked → Locked` auto-lock transition alongside
/// the `Vault` / `Store` (by variant change). Replacement is
/// last-write-wins: a fresh status-line update overwrites any
/// existing one without preserving history.
///
/// Only the `Error` variant lands at this slice; the `Confirmation`
/// variant used by successful modal outcomes per the plan's
/// "Status-line states" list arrives alongside the corresponding
/// modal-effect slices.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum StatusLine {
    /// An action was rejected without changing visible state — e.g.
    /// `n` / `r` / `R` with no account selected. The string is
    /// non-secret prose suitable for verbatim rendering on the bottom
    /// status bar.
    Error(String),
    /// A modal action succeeded and the reducer closed the modal.
    /// Per `IMPLEMENTATION_PLAN_03_TUI.md` "Modals (per §6)" >
    /// "Successful modal outcomes": *"manual Add, URI Add, Remove,
    /// Rename, Export, Passphrase, and Settings close the modal and
    /// publish a status-line confirmation."* The string is
    /// non-secret prose (no OTP digits, no passphrase bytes) built
    /// from the post-save vault state so it matches what a follow-up
    /// `list` would show.
    Confirmation(String),
}

